# 확률변수 X는 표본공간 오메가를 정의역으로 가지고, 실수를 공역으로 가지는 함수
# ex) 주사위를 던졌을 때 눈의 개수 확률변수 X(주사위 눈금 1) = 1

# 이산 확률 분포
# 주사위를 던지면 각 눈이 나올 확률은 1/6
# P(X=1) = p_x(1) = 1/6

# 확률 공리 (모두 참이라고 생각)
# 1. 모든 사건의 확률은 항상 0 보다 크거나 같다
# 2. 표본공간 오메가는 1.0이다.
# 3. 배반사건의 확률은 각각의 확률의 합과 같다

# 조건부 확률 (Conditional probability)
# P(Y|X) = P(Y 교집합 X) / P(X)
# 어떤 사건이 일어났다고 가정했을 때, 다른 사건이 일어날 확률을 의미

# 에이전트의 정책함수 : pi(a|s) = P(A=a|S=s)
# s라는 상태가 주어졌을 때, 행동 a를 고를 확률이 얼마나 되는지에 대한 조건부 확률로 나타냄
